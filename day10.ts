let score = 0;

const matching: { [key: string]: string } = {
    '{': '}',
    '(': ')',
    '[': ']',
    '<': '>',
};

const points: { [key: string]: number } = {
    '}': 1197,
    ')': 3,
    ']': 57,
    '>': 25137,
};

for (const line of lines) {
    let stack = [];
    for (let i=0; i<line.length; i++) {
        let char = line.substr(i, 1);
        for (const [key, value] of Object.entries(matching)) {
            if (char === key) {
                stack.push(char);
            } else if (char === value) {
                if (stack[stack.length - 1] !== key) {
                    console.log('Illegal char: ' + char + ' at ' + i + ' in: ' + line);
                    score += points[char];
                    i = line.length;
                    break;
                }
                stack.pop();
            }
        }
    }
}

